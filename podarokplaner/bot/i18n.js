const dict = { ru, en };

export function normalizeLocale(locale) {
  return locale === 'en' ? 'en' : 'ru';
}

export function t(locale, key, vars = {}) {
  const loc = normalizeLocale(locale);
  let str = dict[loc][key] ?? dict.ru[key] ?? key;
  for (const [k, v] of Object.entries(vars)) {
    str = str.replaceAll(`{${k}}`, String(v));
  }
  return str;
}

export function dateLocale(locale) {
  return normalizeLocale(locale) === 'en' ? 'en-US' : 'ru-RU';
}

export function pluralDaysLabel(locale, n) {
  if (normalizeLocale(locale) === 'en') {
    return n === 1 ? 'day' : 'days';
  }
  if (n === 1) return 'день';
  if (n >= 2 && n <= 4) return 'дня';
  return 'дней';
}

export function detectLocaleFromTelegram(languageCode) {
  return languageCode?.startsWith('en') ? 'en' : 'ru';
}
