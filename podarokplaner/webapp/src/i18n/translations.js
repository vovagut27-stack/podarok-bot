export const translations = { ru, en };

const API_ERROR_MAP = {
  'Нет доступа': 'errors.noAccess',
  'Круг не найден': 'errors.circleNotFound',
  'Имя обязательно': 'errors.nameRequired',
  'Название круга обязательно': 'errors.titleRequired',
  'Лимит бесплатных кругов (3). Оформите Premium!': 'errors.premiumRequired',
  'Сервер недоступен. Попробуйте позже.': 'errors.serverUnavailable',
};

export function translate(locale, key, vars = {}) {
  const dict = translations[locale] || translations.ru;
  let str = dict[key] ?? translations.ru[key] ?? key;
  for (const [k, v] of Object.entries(vars)) {
    str = str.replaceAll(`{${k}}`, String(v));
  }
  return str;
}

export function translateApiError(message, locale) {
  const key = API_ERROR_MAP[message];
  if (key) return translate(locale, key);
  return message;
}

export function dateLocale(locale) {
  return locale === 'en' ? 'en-US' : 'ru-RU';
}

export function detectInitialLocale() {
  try {
    const saved = localStorage.getItem('podarok_locale');
    if (saved === 'en' || saved === 'ru') return saved;
  } catch { /* ignore */ }

  const tgLang = window.Telegram?.WebApp?.initDataUnsafe?.user?.language_code;
  if (tgLang?.startsWith('en')) return 'en';
  return 'ru';
}
